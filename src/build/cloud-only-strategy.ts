import { ExtractedPage, DepthIndexOptions } from '../types/index.js';

const DEPTHINDEX_VERSION = '1.1.6';

export class CloudOnlyStrategy {
  
  /**
   * When mode is 'cloud':
   * - Skip heavy index generation (saves build time & size)
   * - Generate optimized llms.txt for cloud AI context
   * - Generate lightweight page manifest for citation URLs
   */
  async buildForCloud(pages: ExtractedPage[], config: DepthIndexOptions): Promise<CloudBuildOutput> {
    console.log('[DepthIndex] Cloud-only mode: skipping index, generating llms.txt');
    
    // Generate llms.txt (full docs for AI context)
    const llmsFull = this.generateLLMsFull(pages, config);
    
    // Generate llms.jsonl (structured for AI)
    const llmsJSONL = this.generateLLMsJSONL(pages, config);
    
    // Generate page manifest (URLs + titles only, no content)
    const pageManifest = this.generatePageManifest(pages);
    
    return {
      'llms.txt': llmsFull,
      'llms.jsonl': llmsJSONL,
      'page-manifest.json': JSON.stringify(pageManifest),
      // NO depth-index.json.gz — not needed for cloud
    };
  }
  
  private generateLLMsFull(pages: ExtractedPage[], config: DepthIndexOptions): string {
    let output = `# ${config.seo?.siteName || 'Documentation'}\n`;
    output += `# Full documentation for AI context\n`;
    output += `# Generated by DepthIndex v${DEPTHINDEX_VERSION}\n\n`;
    
    for (const page of pages) {
      output += `## ${page.title}\n`;
      output += `URL: ${page.url}\n\n`;
      
      for (const section of page.sections || []) {
        if (section.heading) output += `### ${section.heading}\n`;
        output += `${section.content}\n\n`;
        
        if (section.codeBlocks) {
          for (const block of section.codeBlocks) {
            output += `\`\`\`${block.language}\n${block.code}\n\`\`\`\n\n`;
          }
        }
      }
      output += `---\n\n`;
    }
    
    return output;
  }
  
  private generateLLMsJSONL(pages: ExtractedPage[], config: DepthIndexOptions): string {
    const lines: string[] = [];
    for (const page of pages) {
      lines.push(JSON.stringify({
        url: page.url,
        title: page.title,
        content: page.sections?.map(s => s.content).join('\n\n') || '',
        headings: page.headings?.map(h => h.text) || [],
        hasCode: page.sections?.some(s => s.codeBlocks && s.codeBlocks.length > 0) || false,
      }));
    }
    return lines.join('\n');
  }
  
  private generatePageManifest(pages: ExtractedPage[]): Array<{ url: string; title: string }> {
    return pages.map(p => ({ url: p.url, title: p.title }));
  }
}

export interface CloudBuildOutput {
  'llms.txt': string;
  'llms.jsonl': string;
  'page-manifest.json': string;
}
